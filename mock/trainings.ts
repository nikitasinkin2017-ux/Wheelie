type TrainingSeed = Omit<
  Training,
  'organizer' | 'participants' | 'currentParticipants' | 'isJoined' | 'activity' | 'place' | 'participantCount'
> & {
  organizerId: string;
  participantIds: string[];
};

function makeTraining(seed: TrainingSeed): Training {
  const organizer = getUserById(seed.organizerId);
  const participantIds = Array.from(new Set([seed.organizerId, ...seed.participantIds]));
  const participants = participantIds.map(getUserById);

  return {
    ...seed,
    organizer,
    participants,
    currentParticipants: participants.length,
    isJoined: participantIds.includes(currentUser.id),
    activity: seed.activityType,
    place: seed.startPlace,
    participantCount: participants.length,
  };
}

export function createTrainingFromDraft(draft: TrainingDraft): Training {
  const participants = [currentUser];
  const route = `${draft.startPlace} - маршрут по району ${draft.district}`;

  return {
    ...draft,
    id: `training-${Date.now()}`,
    organizer: currentUser,
    participants,
    currentParticipants: 1,
    isJoined: true,
    startCoordinates: draft.startCoordinates ?? { latitude: 55.7558, longitude: 37.6173 },
    finishCoordinates: draft.finishCoordinates ?? { latitude: 55.7598, longitude: 37.6273 },
    route,
    activity: draft.activityType,
    place: draft.startPlace,
    participantCount: 1,
  };
}

